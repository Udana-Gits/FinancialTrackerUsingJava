    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String reason = (String) jComboBox4.getSelectedItem();
        String amount = jTextField2.getText();
        String year = (String) jComboBox1.getSelectedItem();
        String month = (String) jComboBox2.getSelectedItem();
        String date = (String) jComboBox3.getSelectedItem();
        String note = jTextField3.getText();
        
        int catId =0;
        switch (reason) {
            case "Food":
                catId=1;
                break;
            case "Transportation":
                catId=2;
                break;
            case "Utilitiesg":
                catId=3;
                break;
            case "Housing":
                catId=4;
                break;
            case "Debt Payments":
                catId=5;
                break;
            case "Savings Investments":
                catId=6;
                break;
            case "Health Wellness":
                catId=7;
                break;
            case "Personal Spending":
                catId=8;
                break;
            case "Entertainment Recreation":
                catId=9;
                break;
            case "Education":
                catId=10;
                break;
            default:
                break;
        }
        

        String sql = "INSERT INTO expense (User_Id, Reason, Category_Id, Amount, year, month, date, Note) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
          
        if(!amount.isEmpty()){
            float floatamount = Float.parseFloat(amount);
            if(floatamount<balance){
            try {
             Connection cn = db.getCon();
             PreparedStatement statement = cn.prepareStatement(sql);
            // Prepare the SQL INSERT statement
            statement.setInt(1,UserId);
            statement.setString(2, reason);
            statement.setInt(3,catId);
            statement.setFloat(4, floatamount);  
            statement.setString(5, year);
            statement.setString(6, month);
            statement.setString(7, date);
            statement.setString(8, note);

            // Execute the statement
            statement.executeUpdate();
            String sql1 = "UPDATE login SET Balance = ? WHERE Id = ?";
            PreparedStatement stat = cn.prepareStatement(sql1);
            float newbal = balance - floatamount;
            stat.setFloat(1,newbal);
            stat.setInt(2,UserId);
            stat.executeUpdate();
            
            statement.close();
            stat.close();
            cn.close();
            
            jTextField2.setText("");
            jTextField3.setText("");
            loadData();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        }else{
                JOptionPane.showMessageDialog(null, "Insufficient funds: Your account balance is only Rs."+balance, "Alert", JOptionPane.WARNING_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please fill all important fields", "Alert", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

