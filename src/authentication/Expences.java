    public void loadData(){
        try {
            Connection cn = db.getCon();
            
            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.setRowCount(0);
            String query = "SELECT e.id, e.Reason, e.Amount, e.year, e.month, e.date, e.Note " +
                           "FROM expense e " +
                           "WHERE e.User_ID = ?";
            PreparedStatement pstmt1 = cn.prepareStatement(query);
            pstmt1.setInt(1, UserId);
            ResultSet rs1 = pstmt1.executeQuery();
            
             
            while (rs1.next()) {
                int income_id = rs1.getInt("id");
                String reason = rs1.getString("Reason");
                float amount = rs1.getFloat("Amount");
                String year = rs1.getString("year");
                String month = rs1.getString("month");
                String date = rs1.getString("date");
                String note = rs1.getString("Note");
                String fullDate = year+"/"+month+"/"+date;
                model.addRow(new Object[]{income_id, reason,amount,fullDate, note});
            }
            

            // Close the ResultSet, PreparedStatement, and Connection

            rs1.close();
            pstmt1.close();
            cn.close();
            
        }catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
